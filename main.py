mood = input("What is your mood today? ")

if mood == "happy":
    print("It's great to see you happy!")
elif mood == "sad":
    print("Cheer up!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "excited":
    print("YAY!")
elif mood == "relaxed":
    print("Right on, dude.")
else:
    print("Mood not recognized.")
